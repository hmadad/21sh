#include "shell.h"

void		ft_exit(t_shell **shell)
{
	t_shell	*s;

	s = *shell;
	ft_freetab(&(s->env));
	if (s->line)
		ft_strdel(&(s->line));
	exit(EXIT_SUCCESS);
}
